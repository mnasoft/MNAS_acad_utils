<P>Команда FORMAT предназначена для отрисовки чертежных штампов.</P>
<P><BR></P>
<P>На рисунке 1 представлено главное диалоговое окно программы.</P>
<P ALIGN=CENTER><IMG SRC="format_html_952dae8.png" NAME="Графический объект1" ALIGN=BOTTOM WIDTH=521 HEIGHT=356 BORDER=0></P>
<P ALIGN=CENTER>Рисунок 1 - главное окно диалога программы.</P>

<P><BR></P>
<P>Кнопки главного окна:</P>

<UL>
	<LI><P STYLE="margin-bottom: 0in">"Выбор формата..."; - вызывает диалог выбора формата (см. Рисунок 2);</P>
	<LI><P STYLE="margin-bottom: 0in">"Заполнение ..." - вызывает диалог заполнения текстовых полей чертежного штампа (см. Рисунок 3.);</P>
	<LI><P STYLE="margin-bottom: 0in">"Сохранить настр." - выполняет сохранение в системном реестре выбранных настроек (за исключением) дополнительных граф;</P>
	<LI><P STYLE="margin-bottom: 0in">"Выбрать" - позволяет выбрать чертежный штамп для его последующего редактирования. Данные из чертежного штампа копируются в поля диалога;</P>
	<LI><P STYLE="margin-bottom: 0in">"Указание ..." - позволяет задать в графическом редакторе левый нижний угол отрисовываемого чертежного штампа;</P>
	<LI><P STYLE="margin-bottom: 0in">"Заполнить" - позволяет выбрать в графическом редакторе чертежный штамп, который будет заполнен данными из текущего диалога;</P>
	<LI><P STYLE="margin-bottom: 0in">"Редактирование..." - вызывает диалог выбора дополнительных граф чертежного штампа (см. Рисунок 4);</P>
	<LI><P STYLE="margin-bottom: 0in">"OK" - производит создание штампа в графическом редакторе.</P>
</UL>

<P><BR></P>
<P>Примечание:</P>
<P>Штамп генерируется как непоименованный блок. Изменяемые текстовые поля в штампе формируются в виде атрибутов. После выполнения генерации штампа их можно отредактировать стандартными средствами.</P>

