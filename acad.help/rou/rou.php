<p align="center" style="margin-bottom: 0cm"><font size="4"
style="font-size: 16pt"><u><b>Простановка шероховатостей</b></u><span
style="text-decoration: none"><b>(</b></span><b><span lang="en-US"><span
style="text-decoration: none">ROU)</span></span></b></font></p>

<p align="center" style="margin-bottom: 0cm"><font size="4">(ГОСТ
2789-73)</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">В командной строке
вводим текст <span lang="en-US">«ROU», </span>далее «Ввод».</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">Открывается
диалоговое окно в котором выбираем необходимые параметры.</font></p>

<p align="left" style="margin-bottom: 0cm"><img src="rou_html_m238631cb.jpg"
name="Графический объект1" align="left" width="629" height="406" border="0"><br
clear="left">
<font size="4"><font size="4" style="font-size: 16pt"><b>Параметры и
ряд</b></font> — позволяет выбирать параметр шероховатости <span
lang="en-US">Ra </span>или <span lang="en-US">Rz,</span>так же ряд с 1 по 4.
Используем ряд 2.</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4"><font size="4"
style="font-size: 16pt"><b>Значение параметра</b></font> </font> </p>

<p align="left" style="margin-bottom: 0cm"><font size="4">При указании
<i><u>наибольшего</u></i> параметра шероховатости, необходимо выбрать
«Максимальное» (установить галочку перед текстом «минимальное») и из
предложенного списка выбрать необходимый параметр шероховатости, при этом
параметр шероховатости будет без предельных отклонений.</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">При указании
<i><u>наименьшего</u></i> параметра шероховатости, необходимо выбрать
«Наименьший» (установить галочку перед текстом «максимальное»), при этом будет
проставлен знак шероховатости без обозначения параметра шероховатости.
Обозначение нужно будет внести отдельно однострочным текстом с учетом
выбранного масштаба (размерного стиля). Например <span lang="en-US">Ra
</span>2,5 <span lang="en-US">min.</span></font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4"><span
lang="en-US"></span>При указании способа получения шероховатости, необходимо
дополнительно поставить галочку перед текстом «способ получения» и выбрать из
предложенного списка видов обработки необходимый.</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4"><font size="4"
style="font-size: 16pt"><b>Снятие основного материала</b></font> — позволяет
задать, при обычной обработке материала с выбранной шероховатостью «Есть», при
обработке без снятия материала в сочетании с параметром «Способ получения» -
«Нет»</font></p>

<p align="left" style="margin-bottom: 0cm"><img src="rou_html_8db7857.jpg"
name="Графический объект2" align="left" width="643" height="399" border="0"><br
clear="left">
<font size="4"><font size="4" style="font-size: 16pt"><b>Скобки
вокруг</b></font> — позволяет поставить знак шероховатости в скобках </font>
</p>

<p align="left" style="margin-bottom: 0cm"><font size="4">При необходимости
простановки обработки материала по какому либо замкнутому контуру — ставим
галочку «По контуру».</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">При необходимости
простановки знака шероховатости под размерной линией (например в обозначениях
сварных швов), ставим галочку - «Перевернутый». Данная опция — не
используется.</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4"><font size="4"
style="font-size: 16pt"><b>Направление неровностей</b></font> — позволяет
указать условное обозначение направления неровностей обработки. Условные
обозначения должны соответствовать приведенным в таблице ГОСТ 2.309-73. При
необходимости условные обозначения направления неровностей приводят на чертеже
(штриховкой).</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4"><font size="4"
style="font-size: 16pt"><b>Масштаб</b></font> — позволяет не выходя из команды
простановки шероховатости изменить размерный стиль отображения шероховатости (
используется при простановке знака шероховатости на различных видах выполненных
в различных размерных стилях т.е. масштабах отображения).</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">Клавиша <font
size="4" style="font-size: 16pt"><span
style="font-style: normal"><b>«Проставить»</b></span></font> - позволяет
проставлять знак шероховатости с ранее выбранными параметрами несколько раз,
пока не нажмем «ОК».</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">Клавиша <font
size="4" style="font-size: 16pt"><b>«Выбрать»</b></font> - позволяет
отредактировать уже имеющуюся шероховатость (если она была выполнена при помощи
команды «<span lang="en-US">ROU</span>»).</font></p>

<p align="left" style="margin-bottom: 0cm"><font size="4">Процедура: Нажимаем
«Выбрать», далее на поле чертежа указываем (лкм) шероховатость которую нужно
отредактировать, далее во вновь открывшемся окне «Простановка шероховатостей»
изменяем параметры на необходимые и нажимаем «Обновить». Далее «ОК».</font></p>
