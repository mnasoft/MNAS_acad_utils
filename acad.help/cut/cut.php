<P STYLE="margin-bottom: 0in">Команда CUT генерирует
изображение разреза (или сложного
разреза).</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in">При ее выполнении задаются:</P>
<UL>
	<LI><P STYLE="margin-bottom: 0in">две или несколько точек определяющие расположение разреза
	(или сложного разреза в случае задания более чем двух точек);</P>
	<LI><P STYLE="margin-bottom: 0in">задается точка, определяющая сторону направления взгляда на сечение.</P>
</UL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
